#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst || !del)
		return ;
	if (!lst->content)
		free(lst);
	del(lst->content);
	free(lst);
	return ;
}
