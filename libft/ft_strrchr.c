#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	long	i;

	i = ft_strlen(s);
	if (s[i] == (char)c)
		return ((char *)s + i);
	i--;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
