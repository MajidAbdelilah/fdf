#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*result;

	i = 0;
	result = 0;
	while (s1[i])
		i++;
	i++;
	result = malloc(i);
	if (!result)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = 0;
	return (result);
}
